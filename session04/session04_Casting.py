x = int(1)   # x will be 1
y = int(2.8) # y will be 2
z = int("3") # z will be 3
print(x,y,z)

x1 = float(1)     # x will be 1.0
y1 = float(2.8)   # y will be 2.8
z1 = float("3")   # z will be 3.0
w1 = float("4.2") # w will be 4.2
print(x1,y1,z1,w1)

x2 = str("s1") # x will be 's1'
y2 = str(2)    # y will be '2'
z2 = str(3.0)  # z will be '3.0'
print(x2,y2,z2)