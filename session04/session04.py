xstr = "Hello World"	# Type is: str	
print(type(xstr))
xint = 20	# Type is: int	
print(type(xint))
xfloat = 20.5 # Type is: float	
print(type(xfloat))
xcomplex = 1j	# Type is: complex	
print(type(xcomplex))
xlist = ["apple", "banana", "cherry"]	# Type is: list	
print(type(xlist))
xtuple = ("apple", "banana", "cherry")	# Type is: tuple	
print(type(xtuple))
xrange = range(6)	# Type is: range	
print(type(xrange))
xdict = {"name" : "John", "age" : 36}	# Type is: dict	
print(type(xdict))
xset = {"apple", "banana", "cherry"}	# Type is: set	
print(type(xset))
xfrozenset = frozenset({"apple", "banana", "cherry"})	# Type is: frozenset
print(type(xfrozenset))
xbool1 = True # Type is: bool
print(type(xbool1))	
xbool2 = False # Type is: bool	
print(type(xbool2))
xbytes = b"Hello" # Type is: bytes	
print(type(xbytes))
xbytearray = bytearray(5) # Type is: bytearray	
print(type(xbytearray))
xmemoryview = memoryview(bytes(5)) # Type is: memoryview
print(type(xmemoryview))	
xNoneType = None # Type is: NoneType
print(type(xNoneType))